"""直接处理原始推理数据集，使用全部110个样本"""
import json
import random
from pathlib import Path
from datetime import datetime

print("开始处理原始推理数据集...")

# 文件路径
原始数据路径 = Path("数据/原始数据/推理数据集.json")
输出目录 = Path("数据/处理数据")
输出目录.mkdir(parents=True, exist_ok=True)

# 读取原始数据
print(f"正在读取原始数据: {原始数据路径}")
with open(原始数据路径, 'r', encoding='utf-8') as f:
    原始数据 = json.load(f)

print(f"原始数据样本数: {len(原始数据)}")

# 分析数据结构
if 原始数据:
    print(f"数据结构: {list(原始数据[0].keys())}")

# 定义危险关键词
危险关键词 = [
    '制造', '攻击', '入侵', '诈骗', '歧视', '仇恨', '窃取', '破解', '毒品', '武器',
    '炸弹', '毒药', '枪支', '爆炸物', '伤害', '自杀', '自残', '虐待', '歧视',
    '洗钱', '黑客', '盗窃', '网络钓鱼', '信用卡盗刷', '冒充', '社会工程学',
    '操控', '洗脑', '生化武器', '核武器', '病原体', '病毒', '细菌',
    '恐怖主义', '极端主义', '分裂', '颠覆', '暴乱', '骚乱',
    '色情', '淫秽', '儿童色情', '性骚扰', '性侵犯',
    '诈骗', '欺诈', '传销', '庞氏骗局', '非法集资',
    '间谍', '窃密', '泄密', '叛国', '颠覆国家'
]

# 定义安全关键词
安全关键词 = [
    '学习', '教育', '健康', '健身', '食谱', '烹饪', '旅游', '阅读',
    '编程', '数学', '英语', '科学', '历史', '文化', '艺术', '音乐',
    '工作', '职业', '简历', '面试', '沟通', '管理', '时间管理',
    '生活', '家庭', '朋友', '社交', '心理', '冥想', '放松',
    '环保', '可持续发展', '气候', '自然', '动物', '植物',
    '理财', '投资', '储蓄', '预算', '经济', '金融知识',
    '安全', '防护', '预防', '健康饮食', '运动', '养生'
]

# 处理每个样本
训练数据 = []
危险样本 = []
安全样本 = []

for i, 样本 in enumerate(原始数据, 1):
    提示词 = 样本.get('提示词', '')
    推理过程 = 样本.get('推理过程', '')
    
    # 判断危险程度
    危险得分 = sum(1 for 关键词 in 危险关键词 if 关键词 in 提示词)
    安全得分 = sum(1 for 关键词 in 安全关键词 if 关键词 in 提示词)
    
    标签 = 1 if 危险得分 > 安全得分 else 0
    类型 = "危险" if 标签 == 1 else "安全"
    
    if 标签 == 1:
        危险样本.append(提示词)
    else:
        安全样本.append(提示词)
    
    训练数据.append({
        "id": f"sample_{i:03d}",
        "提示词": 提示词,
        "推理过程": 推理过程,
        "标签": 标签,
        "类型": 类型,
        "样本来源": "原始数据",
        "创建时间": datetime.now().isoformat()
    })

print(f"\n样本统计:")
print(f"危险样本: {len(危险样本)}")
print(f"安全样本: {len(安全样本)}")
print(f"总计: {len(训练数据)}")

# 打乱顺序
random.shuffle(训练数据)

# 拆分数据集
总样本数 = len(训练数据)
训练集样本数 = int(总样本数 * 0.7)
验证集样本数 = int(总样本数 * 0.15)

训练集 = 训练数据[:训练集样本数]
验证集 = 训练数据[训练集样本数:训练集样本数+验证集样本数]
测试集 = 训练数据[训练集样本数+验证集样本数:]

print(f"\n数据集拆分:")
print(f"训练集: {len(训练集)} 个样本")
print(f"验证集: {len(验证集)} 个样本")
print(f"测试集: {len(测试集)} 个样本")

# 保存训练集
训练集路径 = 输出目录 / "训练集.json"
with open(训练集路径, 'w', encoding='utf-8') as f:
    json.dump(训练集, f, ensure_ascii=False, indent=2)
print(f"✅ 训练集已保存: {训练集路径}")

# 保存验证集
验证集路径 = 输出目录 / "验证集.json"
with open(验证集路径, 'w', encoding='utf-8') as f:
    json.dump(验证集, f, ensure_ascii=False, indent=2)
print(f"✅ 验证集已保存: {验证集路径}")

# 保存测试集
测试集路径 = 输出目录 / "测试集.json"
with open(测试集路径, 'w', encoding='utf-8') as f:
    json.dump(测试集, f, ensure_ascii=False, indent=2)
print(f"✅ 测试集已保存: {测试集路径}")

# 生成数据报告
报告内容 = f"""📊 数据处理报告
生成时间: {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}
{'='*50}

📁 原始数据:
   总样本数: {总样本数}
   危险样本: {len(危险样本)}
   安全样本: {len(安全样本)}

📁 最终数据集:
   训练集: {len(训练集)} 个样本
   验证集: {len(验证集)} 个样本
   测试集: {len(测试集)} 个样本
   总计: {len(训练集) + len(验证集) + len(测试集)} 个样本

📄 数据示例:
"""

# 添加示例
for i, 样本 in enumerate(训练集[:3], 1):
    报告内容 += f"""
示例 {i} [{'🔴 危险' if 样本['标签'] == 1 else '🟢 安全'}]:
   提示词: {样本['提示词'][:50]}...
   推理: {样本['推理过程'][:60]}...
   来源: {样本.get('样本来源', 'N/A')}
"""

报告路径 = 输出目录 / "数据报告.txt"
with open(报告路径, 'w', encoding='utf-8') as f:
    f.write(报告内容)

print(f"\n✅ 数据报告已保存: {报告路径}")
print("\n🎉 数据处理完成！")
